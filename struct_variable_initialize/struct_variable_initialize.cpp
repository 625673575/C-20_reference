struct A { int x, y; };
struct B { int y, x; };

void f(A a, int) {} // #1
void f(B b, ...) {} // #2
void g(A a) {} // #3
void g(B b) {} // #4

//在构造对象时，可以指定成员进行初始化，但是初始化的顺序必须与成员的内存顺序一致
int main()
{
    f({ .x = 1, .y = 2 }, 0); // 正确，调用#1
    //f({ .y = 1, .x = 2 }, 0); // 错误，调用#1，初始化顺序不匹配
    f({ .y = 1, .x = 2 }, 1, 2, 3); // 正确，调用#2
    //g({ .x = 1, .y = 2 }); // 错误，无法确定调用#3还是#4
    return 0;
}
